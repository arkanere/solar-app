	<h2>Frequently Asked Questions</h2>

	<div class="faq-item">
		<h3>What is the cost of a Solar Panel System?</h3>
		<p>Approximate total cost of Solar On Grid System installation:</p>
		<ul>
			<li>1kW: 72k - 77k</li>
			<li>2kW: 144k - 158k</li>
			<li>3kW: 216k - 240k</li>
			<li>4kW: 268k - 296k</li>
			<li>5kW: 336k - 370k</li>
		</ul>
		<p>For requirements above 5kW, the total cost per kW is approximately Rs 60k to Rs 65k.</p>
	</div>

	<div class="faq-item">
		<h3>What is the size of the Solar System Should I install?</h3>
		<p>
			Decision on the size of Solar Panel System should be based on average monthly electricity
			usage:
		</p>
		<ul>
			<li>0-150 kWh/units: 1kW – 2kW</li>
			<li>150-300 kWh/units: 2kW – 3kW</li>
			<li>&gt;300 kWh/units: Above 3kW</li>
		</ul>
		<p>
			1kW of solar panel system generates approximately 90-110 kWh/units of electricity per month.
		</p>
	</div>

	<div class="faq-item">
		<h3>Checklist to install Solar Panel System</h3>
		<ul>
			<li>Should have enough shade-free area. Need around 100 sq. feet per kW.</li>
			<li>
				Should have an electricity connection with sanction load at least equal to solar system
				size.
			</li>
		</ul>
	</div>

	<div class="faq-item">
		<h3>Checklist to qualify for subsidy under PM Surya Ghar Yojana</h3>
		<ul>
			<li>Should have an active electricity connection with a discom company.</li>
			<li>Minimum solar system size installed should be 1kW.</li>
			<li>Should not have availed subsidy before for solar-related projects.</li>
			<li>Installation should be done by an empanelled solar panel installer.</li>
			<li>Components used should be manufactured by companies in the ALMM list.</li>
		</ul>
	</div>

	<div class="faq-item">
		<h3>What is net metering?</h3>
		<p>
			Net metering is a billing arrangement that enables homeowners with solar panel systems to
			receive credit for excess electricity they generate and feed back into the grid.
		</p>
		<p>
			If you use 80 units of electricity in a month but your solar panels produce 100 units, you
			won't get a bill for that month, and the extra 20 units will be saved as credits for future
			use.
		</p>
		<p>
			If you use 120 units but only generate 100 units, you'll need to pay for the extra 20 units
			unless you have credits from previous months.
		</p>
	</div>

	<div class="faq-item">
		<h3>What appliances can run on the on-grid solar system?</h3>
		<p>
			You can keep using all the appliances in your home without any interruption. This includes:
		</p>
		<ul>
			<li>
				Tube lights, LED bulbs, induction cookers, refrigerators, washing machines, air
				conditioners, and even electric vehicles.
			</li>
			<li>
				The on-grid solar system is connected to the grid, so any extra energy you need is supplied
				by the grid.
			</li>
		</ul>
	</div>

	<div class="faq-item">
		<h3>How is installing a Solar Panel System financially beneficial?</h3>
		<p>
			Installing a solar system will make your electricity cheaper than buying it from the utility
			company.
		</p>
		<p>
			Example: A 3kW system costs about ₹1.5 lakh after subsidies, and generates around 250 units of
			electricity per month, saving about ₹2300 per month.
		</p>
	</div>

	<div class="faq-item">
		<h3>How will my cash flows change if I install a solar system on EMI?</h3>
		<p>
			Example: You install a 3kW solar panel system costing ₹225,000, get a subsidy of ₹78,000, and
			pay the rest through EMI.
		</p>
		<p>1st month: ₹22,500 down payment + ₹4780 EMI</p>
		<p>After subsidy: EMI reduces to ₹2875 for the rest of the payback period.</p>
	</div>

	<div class="faq-item">
		<h3>What is the ALMM list?</h3>
		<p>
			The Ministry of New and Renewable Energy (MNRE) issued the ALMM order, which lists approved
			models and manufacturers for solar photovoltaic modules to ensure reliability and quality.
		</p>
	</div>

	<div class="faq-item">
		<h3>What is an empanelled solar panel installer?</h3>
		<p>
			An empanelled solar panel installer is one that is registered with Distribution companies and
			meets specific qualification criteria as per MNRE orders.
		</p>
	</div>

	<div class="faq-item">
		<h3>What is the warranty on solar panels and inverters?</h3>
		<p>
			Performance warranty on solar panel modules is usually between 20 to 25 years. Inverters come
			with a warranty of 7 to 10 years.
		</p>
	</div>

	<div class="faq-item">
		<h3>How is a solar system's performance in the rainy season?</h3>
		<p>
			Solar panels produce less electricity during the rainy season but do not stop working. Solar
			systems are designed to factor in yearly electricity generation and net metering policies.
		</p>
	</div>

	<div class="faq-item">
		<h3>What is the maintenance requirement of solar panel systems?</h3>
		<p>
			Solar panel systems require minimal maintenance. Regular cleaning and trimming of nearby trees
			or obstacles to prevent shading are important for optimal performance.
		</p>
	</div>

