		<ul>
			<li>
				<strong>Government-Recognized Training:</strong> Look for installers certified by the Skill Council
				for Green Jobs (SCGJ) or empaneled by MNRE.
			</li>
			<li>
				<strong>Manufacturer Certifications:</strong> Reputable brands authorize certain installers who
				meet strict quality benchmarks.
			</li>
			<li>
				<strong>Professional Affiliations:</strong> Membership in industry bodies (e.g., Indian Solar
				Association) reflects a commitment to best practices.
			</li>
			<li>
				<strong>Local Experience:</strong> Installers familiar with regional climates and regulations
				can better handle site-specific needs.
			</li>
			<li>
				<strong>Client Reviews:</strong> Check portfolios, testimonials, and case studies. Positive user
				feedback is a strong indicator of trustworthiness.
			</li>
		</ul>

