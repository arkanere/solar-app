		<h2>1. Role of Solar Panel Installers</h2>
		<p>
			Solar panel installers coordinate everything from evaluating the site to completing system
			checks:
		</p>
		<ul>
			<li>
				<strong>Site Assessment:</strong> They gauge roof strength, tilt angles, and shade levels to
				maximize sunlight exposure.
			</li>
			<li>
				<strong>System Design:</strong> By studying your electricity usage, they recommend the optimal
				number and type of panels, plus inverters and mounting structures.
			</li>
			<li>
				<strong>Regulations &amp; Permits:</strong> Installers handle paperwork for net metering, subsidies,
				and city permits.
			</li>
			<li>
				<strong>Installation &amp; Testing:</strong> Teams install rails, panels, and wiring, then confirm
				the system’s performance and safety.
			</li>
			<li>
				<strong>Education:</strong> They brief you on system monitoring, basic troubleshooting, and maintenance
				schedules.
			</li>
		</ul>

