    <h3>Phase 3: Net Meter Installation</h3>
    <ul>
      <li>
        <strong>Meter Installation:</strong> DISCOM installs bidirectional meter
      </li>
      <li>
        <strong>Testing and Commissioning:</strong> System performance validation
      </li>
      <li>
        <strong>Grid Synchronization:</strong> Connection to distribution network
      </li>
