    <h2>Net Metering Billing Process in India</h2>
    <p>
      The billing process under net metering is transparent and
      consumer-friendly:
    </p>

    <h3>Monthly Billing Cycle:</h3>
    <ul>
      <li>
        <strong>Energy Import:</strong> Units consumed from the grid (kWh imported)
      </li>
      <li>
        <strong>Energy Export:</strong> Units exported to the grid (kWh exported)
      </li>
      <li><strong>Net Consumption:</strong> Import - Export = Net units</li>
      <li>
        <strong>Bill Calculation:</strong> Net units × applicable tariff rate
      </li>
    </ul>

    <h3>Billing Scenarios:</h3>
    <ul>
      <li><strong>Net Import:</strong> Pay for net units consumed from grid</li>
      <li>
        <strong>Net Export:</strong> Receive credits for net units exported
      </li>
      <li>
        <strong>Zero Net:</strong> Pay only fixed charges and applicable taxes
      </li>
    </ul>

    <h3>Credit System:</h3>
    <ul>
      <li>
        <strong>Credit Rate:</strong> Generally equivalent to average power purchase
        cost
      </li>
      <li>
        <strong>Credit Validity:</strong> Usually carried forward for 12 months
      </li>
      <li>
        <strong>Credit Utilization:</strong> Applied against future electricity consumption
      </li>
      <li>
        <strong>Banking System:</strong> Excess credits accumulated during high generation
        months
      </li>
    </ul>

    <h2>Types of Net Metering in India</h2>

    <h3>1. Gross Metering</h3>
    <ul>
      <li>
        <strong>Concept:</strong> All solar energy is exported to grid, all consumption
        from grid
      </li>
      <li>
        <strong>Payment:</strong> Feed-in tariff for entire solar generation
      </li>
      <li>
        <strong>Billing:</strong> Separate meters for generation and consumption
      </li>
      <li>
        <strong>Applicability:</strong> Commercial and industrial installations
      </li>
    </ul>

    <h3>2. Net Metering</h3>
    <ul>
      <li>
        <strong>Concept:</strong> Solar energy first meets load, surplus exported
      </li>
      <li>
        <strong>Payment:</strong> Credits for net export at avoided cost rates
      </li>
      <li>
        <strong>Billing:</strong> Single bidirectional meter for net measurement
      </li>
      <li>
        <strong>Applicability:</strong> Residential and small commercial systems
      </li>
    </ul>

    <h3>3. Virtual Net Metering</h3>
    <ul>
      <li>
        <strong>Concept:</strong> Solar credits shared across multiple consumers
      </li>
      <li>
        <strong>Application:</strong> Apartment complexes and housing societies
      </li>
      <li>
        <strong>Benefits:</strong> Shared solar installation costs and benefits
      </li>
      <li>
        <strong>Implementation:</strong> Single system serving multiple billing units
      </li>
    </ul>

