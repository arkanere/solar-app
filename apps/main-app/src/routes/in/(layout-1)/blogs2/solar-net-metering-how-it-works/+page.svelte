        <strong>Approval Letter:</strong> Connection agreement issued within 30 days
      </li>
      <li>
        <strong>Application Fee:</strong> Nominal processing fee as per DISCOM rates
      </li>
    </ul>

    <h3>Phase 2: Solar System Installation</h3>
    <ul>
